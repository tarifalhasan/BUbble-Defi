// Add a new endpoint to get the balance of a specific address
app.get('/balance/:address', async (req, res) => {
  const { address } = req.params;

  try {
    // Get the balance of the address using the web3 library
    const balance = await web3.eth.getBalance(address);

    // Return the balance as a JSON response
    res.json({ balance });
  } catch (error) {
    // If there was an error, return a default response
    res.json({ error: 'Unable to get balance' });
  }
});

// Add a new endpoint to get the metadata for a specific NFT
app.get('/nft/:id', async (req, res) => {
  const { id } = req.params;

  try {
    // Create a new instance of the NFT contract using the web3 library
    const nftContract = new web3.eth.Contract(nftABI, nftContractAddress);

    // Get the metadata for the NFT using the contract instance
    const metadata = await nftContract.methods.getMetadata(id).call();

    // Return the metadata as a JSON response
    res.json({ metadata });
  } catch (error) {
    // If there was an error, return a default response
    res.json({ error: 'Unable to get NFT metadata' });
  }
});

